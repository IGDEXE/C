// Criacao de variaveis
// Biblioteca
#include <stdio.h>
// Funcao principal
int main(int argc, char const *argv[])
{
	// Variavel do tipo caracter
	char character = 'a';
	printf("Letra: %c\n", character);
	// Variavel do tipo Inteiro
	int integer = 1;
	printf("Inteiro: %d\n", integer);
	// Variavel do tipo real/ponto flutuante
	float floatNumber = 2.5693f;
	printf("Real: %f\n", floatNumber);
	// Variavel do tipo Double
	double doubleNumber = 3.14159;
	printf("Doble: %f\n", doubleNumber);
	return 0;
}